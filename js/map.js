var k=0;
function Map()
{
	_divMain = document.getElementById("Main");
	_divMain.innerHTML = "";
	for(var i=0;i<a[k].length;i++){
		for(var j=0;j<a[k][i].length;j++)
		{
			var map=document.createElement("div");
				map.style.position="absolute";
				map.setAttribute('class','wrapper');
				map.style.top=40*i+50+"px";
				map.style.left=40*j+50+"px";
				map.style.width="40px";
				map.style.height="40px";
				map.style.background="url('img/road.png')";
				map.style.zIndex="10";
				map.x=i;
				map.y=j;
				map.id="map"+i+j+k;
				_divMain.appendChild(map);
			if(a[k][i][j]==0)//路
			{
				map.style.background="url('img/road.png')";
				}
			if(a[k][i][j]==1)//墙
			{
				map.style.background="url('img/wall.png')";
				}
			if(a[k][i][j]==2)//坦克1号
			{
				var _div=document.createElement("div");
					_div.id="tank1";
					_div.x=i;
					_div.y=j;
					_div.fx=3;
					_div.style.position="absolute";
					_div.style.top=40*i+50+"px";
					_div.style.left=40*j+50+"px";
					_div.style.width="40px";
					_div.style.height="40px";
					_divMain.appendChild(_div);
					_div.style.zIndex="100";
					_div.style.background="url('img/tank1-up.png')";
				}
			if(a[k][i][j]==4)//坦克2号
			{
				var _div=document.createElement("div");
					_div.id="tank2";
					_div.x=i;
					_div.y=j;
					_div.fy=3;
					_div.style.position="absolute";
					_div.style.top=40*i+50+"px";
					_div.style.left=40*j+50+"px";
					_div.style.width="40px";
					_div.style.height="40px";
					_divMain.appendChild(_div);
					_div.style.zIndex="100";
					_div.style.background="url('img/tank2-up.png')";
				}
			if(a[k][i][j]==3)//终点
			{
				map.style.background="url('img/win.png')";
			}
			var temp=Math.round(Math.random()*100);
			if(temp>98.98)//补给
			{
				if(a[k][i][j]==0){
				a[k][i][j]=5;
				map.style.background="url('img/ammo.png')";
				}
			}
			}
		}
	}